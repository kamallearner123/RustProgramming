//! This module provides basic arithmetic functions.
pub mod math {
    /// Subtracts one number from another.
    ///
    /// # Arguments
    ///
    /// * `a` - The number to subtract from.
    /// * `b` - The number to subtract.
    ///
    /// # Returns
    ///
    /// The difference between `a` and `b`.
    pub fn subtract(a: i32, b: i32) -> i32 {
        a - b
    }
}
